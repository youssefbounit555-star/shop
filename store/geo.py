def get_locale_strings(language_code: str) -> dict[str, str]:
    normalized = (language_code or "en").lower()
    return LOCALIZED_STRINGS.get(normalized, LOCALIZED_STRINGS["en"])


def get_preferred_language(request, fallback: str) -> str:
    accepted = request.META.get("HTTP_ACCEPT_LANGUAGE", "")
    if not accepted:
        return fallback

    candidates = []
    for chunk in accepted.split(","):
        code = chunk.split(";")[0].strip().lower()
        if not code:
            continue
        base = code.split("-")[0]
        candidates.append(base)

    supported = set(LOCALIZED_STRINGS.keys())
    for code in candidates:
        if code in supported:
            return code

    return fallback


def override_profile_language(profile: GeoProfile, language: str) -> GeoProfile:
    if not language:
        return profile

    language = language.lower()
    rtl_languages = {"ar", "he", "fa", "ur"}
    direction = "rtl" if language in rtl_languages else "ltr"
    return GeoProfile(
        country=profile.country,
        language=language,
        currency=profile.currency,
        currency_label=profile.currency_label,
        theme=profile.theme,
        direction=direction,
    )


def _get_client_ip(request) -> str:
    forwarded_for = request.META.get("HTTP_X_FORWARDED_FOR", "")
    if forwarded_for:
        return forwarded_for.split(",")[0].strip()
    return request.META.get("REMOTE_ADDR", "") or ""


def _country_from_headers(request) -> str | None:
    header_candidates = (
        "HTTP_CF_IPCOUNTRY",
        "HTTP_X_COUNTRY_CODE",
        "HTTP_X_GEO_COUNTRY",
        "HTTP_X_APPENGINE_COUNTRY",
    )
    for header in header_candidates:
        code = request.META.get(header)
        if not code:
            continue
        code = str(code).strip().upper()
        if len(code) == 2 and code != "XX":
            return code
    return None


def _country_from_api(ip_address: str) -> str | None:
    api_url = str(getattr(settings, "GEOIP_API_URL", "") or "").strip()
    if not api_url or not ip_address:
        return None

    request_url = api_url.format(ip=ip_address)
    params = {}
    api_key = str(getattr(settings, "GEOIP_API_KEY", "") or "").strip()
    api_key_param = str(getattr(settings, "GEOIP_API_KEY_PARAM", "key") or "key").strip()
    if api_key:
        params[api_key_param] = api_key

    try:
        response = requests.get(request_url, params=params, timeout=2)
        if response.status_code != 200:
            return None
        payload: dict[str, Any] = response.json()
    except Exception:  # pragma: no cover - network dependency
        logger.warning("GeoIP API request failed", exc_info=True)
        return None

    for key in ("country_code", "country", "countryCode", "country_code2"):
        code = payload.get(key)
        if isinstance(code, str) and len(code) == 2:
            return code.upper()
    return None


def _get_geo_profiles() -> dict[str, dict[str, str]]:
    custom_profiles = getattr(settings, "GEO_COUNTRY_PROFILES", None)
    if isinstance(custom_profiles, dict) and custom_profiles:
        merged = {**DEFAULT_GEO_PROFILES, **custom_profiles}
        return merged
    return DEFAULT_GEO_PROFILES


def _build_profile(country_code: str | None) -> GeoProfile:
    profiles = _get_geo_profiles()
    normalized = (country_code or "").upper()
    base = profiles.get(normalized, profiles.get("DEFAULT", DEFAULT_GEO_PROFILES["DEFAULT"]))
    language = base.get("language", "en")
    currency = base.get("currency", "USD")
    currency_label = base.get("currency_label") or currency
    theme = base.get("theme", "global")
    direction = base.get("direction") or ("rtl" if language == "ar" else "ltr")
    country = normalized or base.get("country", "DEFAULT")
    return GeoProfile(
        country=country,
        language=language,
        currency=currency,
        currency_label=currency_label,
        theme=theme,
        direction=direction,
    )


def get_geo_profile(request) -> GeoProfile:
    cache_key = "geo_profile"
    ts_key = "geo_profile_ts"
    ttl_seconds = int(getattr(settings, "GEO_PROFILE_TTL", 60 * 60 * 24))
    now_ts = int(timezone.now().timestamp())

    cached = request.session.get(cache_key)
    cached_ts = request.session.get(ts_key)
    if isinstance(cached, dict) and isinstance(cached_ts, int):
        if now_ts - cached_ts < ttl_seconds:
            return _build_profile(cached.get("country"))

    override = str(getattr(settings, "GEO_COUNTRY_OVERRIDE", "") or "").strip().upper()
    country_code = override or _country_from_headers(request)
    if not country_code:
        country_code = _country_from_api(_get_client_ip(request))

    profile = _build_profile(country_code)
    request.session[cache_key] = {"country": profile.country}
    request.session[ts_key] = now_ts
    request.session.modified = True
    return profile
